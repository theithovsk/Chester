    private void finalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finalizarActionPerformed

        int linha = tabVeiculos2.getSelectedRow();

        if (tabVeiculos2.getValueAt(linha, 6).equals("S")) {

            GPLACA = (tabVeiculos2.getValueAt(linha, 5)).toString();
            GTAXA = (tabVeiculos2.getValueAt(linha, 4)).toString();
            String marcaCarro = (tabVeiculos2.getValueAt(linha, 0)).toString();
            String modeloCarro = (tabVeiculos2.getValueAt(linha, 1)).toString();
            String corCarro = (tabVeiculos2.getValueAt(linha, 2)).toString();
            String categoriaCarro = (tabVeiculos2.getValueAt(linha, 3)).toString();

            tabVeiculos2.setValueAt("N", linha, 6);

            System.out.println(GPLACA);

            Date datasistema = new Date();
            SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
            System.out.println((formato.format(datasistema)));

            Calendar now = Calendar.getInstance();
            System.out.println((String.format("%1$tH:%1$tM:%1$tS", now)));

            String horaAluguel = String.format("%1$tH:%1$tM:%1$tS", now);
            String dataAluguel = formato.format(datasistema);

            Alugar(GRG, GPLACA, GTAXA, horaAluguel, dataAluguel);

            JOptionPane.showMessageDialog(null, "Locação Realizada com sucesso!");
            this.dispose();

            //////
            //início
            //Procura a linha em que a placa está cadastrada e reescreve ela
            String oldFileName = "carros.txt";
            String tmpFileName = "tmp_carros.txt";

            BufferedReader br = null;
            BufferedWriter bw = null;

            try {
                br = new BufferedReader(new FileReader(oldFileName));
                bw = new BufferedWriter(new FileWriter(tmpFileName));
                String line;
                while ((line = br.readLine()) != null) {
                    if (line.contains(GPLACA)) {
                        line = line.replaceAll(line, (marcaCarro + ";" + modeloCarro +
                                ";" + corCarro + ";" + categoriaCarro + ";" + GTAXA +
                                ";" + GPLACA + ";" + "N"));
                    }
                    bw.write(line + "\n");
                }
            } catch (Exception e) {
                return;
            } finally {
                try {
                    if (br != null) {
                        br.close();
                    }
                } catch (IOException e) {
                    //
                }
                try {
                    if (bw != null) {
                        bw.close();
                    }
                } catch (IOException e) {
                    //
                }
            }
            //deleta o arquivo antigo
            File oldFile = new File(oldFileName);
            oldFile.delete();

            //Renomeia o arquivo temporário
            File newFile = new File(tmpFileName);
            newFile.renameTo(oldFile);

            //fim
            //////
        } else {
            JOptionPane.showMessageDialog(null, "Veículo encontra-se indisponível!");
        }

    }//GEN-LAST:event_finalizarActionPerformed

