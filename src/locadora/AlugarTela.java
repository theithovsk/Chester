    public void lerClientes() {
        try {
            InputStream is = new FileInputStream("clientes.txt");
            InputStreamReader isr = new InputStreamReader(is);
            BufferedReader br = new BufferedReader(isr);

            String b = br.readLine();

            while (b != null) {
                temp = new String[5];
                temp = b.split(";");

                carregarCliente();

                b = br.readLine();
            }
            br.close();
        } catch (Exception e) {
        }
    }

