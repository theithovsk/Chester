    public void Alugar(String rg, String Placa, String Taxa, String horaAluguel, String dataAluguel) {
        File file = new File("Alugados.txt");
        try {
            int linha = tabVeiculos2.getSelectedRow();
            GPLACA = (tabVeiculos2.getValueAt(linha, 5)).toString();
            GTAXA = (tabVeiculos2.getValueAt(linha, 4)).toString();
            String marcaCarro = (tabVeiculos2.getValueAt(linha, 0)).toString();
            String modeloCarro = (tabVeiculos2.getValueAt(linha, 1)).toString();
            String corCarro = (tabVeiculos2.getValueAt(linha, 2)).toString();
            String categoriaCarro = (tabVeiculos2.getValueAt(linha, 3)).toString();

            FileWriter fileWriter = new FileWriter("Alugados.txt", true);
            BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);
            bufferedWriter.write(GRG + ";" + GPLACA + ";" + GTAXA + ";" + horaAluguel + 
                    ";" + dataAluguel + ";" + marcaCarro + ";" + modeloCarro + ";" +
                    corCarro + ";" + categoriaCarro + ";");
            bufferedWriter.newLine();
            bufferedWriter.close();
        } catch (HeadlessException | IOException e) {
            System.out.println("Erro: " + e);
        }
    }

