    private void btnCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarClienteActionPerformed
        //recebe os dados do cliente

        String nomeCliente = txtNomeCliente.getText();
        String rgCliente = txtRgCliente.getText();
        String cpfCliente = txtCpfCliente.getText();
        String cnhCliente = txtCnhCliente.getText();
        String foneCliente = txtFoneCliente.getText();

        Cliente nclientes = new Cliente(nomeCliente, rgCliente, cpfCliente, cnhCliente,
                foneCliente);

        clientes[qtd] = nclientes;
        qtd++;

        JOptionPane.showMessageDialog(null, "Cliente cadastrado!");

        //apaga dados
        txtNomeCliente.setText("");
        txtRgCliente.setText("");
        txtCpfCliente.setText("");
        txtCnhCliente.setText("");
        txtFoneCliente.setText("");

    }//GEN-LAST:event_btnCadastrarClienteActionPerformed

