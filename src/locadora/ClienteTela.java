    public void persistir() {
        File file = new File("clientes.txt");

        for (int i = 0; i < qtd; i++) {
            try {

                Cliente nclientes = clientes[i];

                //verifica se o arquivo jÃ¡ existe e escreve no final do mesmo
                FileWriter fileWriter = new FileWriter("clientes.txt", true);
                BufferedWriter bufferedWriter = new BufferedWriter(fileWriter);

                bufferedWriter.write(nclientes.getNome() + ";" + nclientes.getRg()
                        + ";" + nclientes.getCpf() + ";" + nclientes.getCnh() + ";"
                        + nclientes.getFone() + ";");
                bufferedWriter.newLine();
                bufferedWriter.close();

            } catch (HeadlessException | IOException e) {
                System.out.println("Erro: " + e);
            }
        }
    }

